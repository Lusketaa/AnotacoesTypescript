// Typescript

// Linguagem fortemente tipada
// É um superset do JavScript, podemos utilizá-lo no Front-end, Back-end e Mobile.
// O código é compilado e transformado para Js no browser ou em Node.

// COMANDOS:
// tsc .\index.ts Para compilar o ts e gerar um arquivo Js
// tsc .\index.ts --outDir dist Para compilar o código Ts em outra pasta
// tsc .\index.ts .\cliente.ts Compila mais de um arquivo

// Node:
// node .\index.js Para compilar utilizando o motor do node para gerar uma saída com Js
// npm install -g nodemon
// nodemon .\caminho para arquivo js para inicializar

// tsconfig.json:
// tsc --init Para criar um arquivo tsconfig.json que traz configurações uteis para um projeto em Ts | Após isso o comando tsc já compila todos os arquivos do projeto
// tsc --watch ou tsc -w vai atualizar os arquivos automaticamente 